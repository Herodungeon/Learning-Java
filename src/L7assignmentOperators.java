public class L7assignmentOperators {
    public static void main(String[] args) {

        int x = 100;
        int y = 20;

        x += y;
        System.out.println("x = " + x);
        x -= y;
        System.out.println("x = " + x);
        x *= y;
        System.out.println("x = " + x);
        x /= y;
        System.out.println("x = " + x);
        x %= y;
        System.out.println("x = " + x);

        x++;
        System.out.println("x = " + x);

        x--;
        System.out.println("x = " + x);
    }
}
